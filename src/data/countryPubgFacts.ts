// ---------------------------------------------------------------------------
// getCountryPubgFact — returns curated data or a deterministic fallback that
// still uses native-script keywords for the country in question.
// ---------------------------------------------------------------------------
export function getCountryPubgFact(
  countryCode: string | undefined,
  countryName: string,
  currency: string,
  paymentMethods: string[],
): CountryPubgFact {
  const upper = (countryCode || 'US').toUpperCase();
  const curated = CURATED_FACTS[upper];
  if (curated) return curated;

  const pack = LANG_BY_COUNTRY[upper] || DEFAULT_LANG;
  const localPayments = (paymentMethods && paymentMethods.length ? paymentMethods : ['Visa', 'MasterCard']).slice(0, 5);

  // Deterministic pseudo-random derived from the code — same input, same output.
  const codeSum = upper.split('').reduce((a, c) => a + c.charCodeAt(0), 0);
  const peakStart = 18 + (codeSum % 5); // 18–22 local hour
  const peakEnd = (peakStart + 4) % 24;
  const nicknameSeeds = ['Falcons', 'Snipers', 'Warriors', 'Legends', 'Raiders', 'Titans', 'Hunters', 'Guardians'];
  const nickname = `${upper} ${nicknameSeeds[codeSum % nicknameSeeds.length]}`;

  return {
    capital: countryName,
    cities: [countryName],
    nativeKeywords: [
      `${pack.buy} ${countryName}`,
      `${pack.cheap} PUBG UC ${countryName}`,
      `${pack.buy} ${currency}`,
      `${pack.buy} ${localPayments[0] || 'online'}`,
      `${pack.cheap} UC ${pack.script}`,
    ],
    localPayments,
    esportsNote: `Local PUBG Mobile qualifiers in ${countryName} feed into regional PMPL circuits.`,
    playerNickname: nickname,
    peakHours: `${String(peakStart).padStart(2, '0')}:00–${String(peakEnd).padStart(2, '0')}:00 local time`,
    regionalRivals: [],
  };
}
