// Helper function to get translation for a country
export function getEmailTranslation(countryCode?: string): EmailTranslation {
  if (!countryCode) return EMAIL_TRANSLATIONS.en;
  
  const upperCountry = countryCode.toUpperCase();
  const languageCode = COUNTRY_TO_LANGUAGE[upperCountry] || 'en';
  
  return EMAIL_TRANSLATIONS[languageCode] || EMAIL_TRANSLATIONS.en;
}

// Helper function to get language code from country
export function getLanguageFromCountry(countryCode?: string): string {
  if (!countryCode) return 'en';
  const upperCountry = countryCode.toUpperCase();
  return COUNTRY_TO_LANGUAGE[upperCountry] || 'en';
}
